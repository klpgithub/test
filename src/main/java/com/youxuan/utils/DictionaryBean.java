package com.youxuan.utils;


public class DictionaryBean {

	private String version;
	private String label;
	private String name;
	private String note;
	private String recordTypeStart;
	private String recordTypeLen;
	private String positions;
	private String zeroFill;
	private String decimalChar;
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getRecordTypeStart() {
		return recordTypeStart;
	}
	public void setRecordTypeStart(String recordTypeStart) {
		this.recordTypeStart = recordTypeStart;
	}
	public String getRecordTypeLen() {
		return recordTypeLen;
	}
	public void setRecordTypeLen(String recordTypeLen) {
		this.recordTypeLen = recordTypeLen;
	}
	public String getPositions() {
		return positions;
	}
	public void setPositions(String positions) {
		this.positions = positions;
	}
	public String getZeroFill() {
		return zeroFill;
	}
	public void setZeroFill(String zeroFill) {
		this.zeroFill = zeroFill;
	}
	public String getDecimalChar() {
		return decimalChar;
	}
	public void setDecimalChar(String decimalChar) {
		this.decimalChar = decimalChar;
	}
	

}
